public class Mathusingmethod {
    public static void main(String[] args) {
         double   a=30;
         double   c=4.5;
        System.out.println("Maximum value is:"+Math.max(a,c));
        System.out.println("Minimum value is:"+Math.min(a,c));
        System.out.println("Square number is:"+Math.sqrt(a));
        System.out.println("Power number is of number:"+Math.pow(a,c));
        System.out.println("Sin is number of:"+Math.sin(a));
        System.out.println("The cube is :"+Math.cbrt(c));
        System.out.println("The round number is:"+Math.round(a));
        System.out.println("The absolute value is:"+Math.abs(a));
        System.out.println("it is used to smallest number:"+Math.ceil(c));
        System.out.println("It is used to is:"+Math.floor(c));
        System.out.println("Square number is "+Math.hypot(c,a));
        System.out.println("Mathematical integer is:"+Math.rint(a));
        System.out.println("Sine number is:"+Math.cos(a));
        System.out.println("Tan number is :"+Math.tan(c));
        System.out.println("Cat number is :"+Math.acos(c));
    }
}
